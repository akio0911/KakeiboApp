import UIKit

extension UIColor{
    //赤系
    class var orangeRedCrayola: UIColor {
        return UIColor(red: 225/225, green: 94/225, blue: 91/225, alpha: 1)
    }
    //濃い青系
    class var spaceCadet: UIColor {
        return UIColor(red: 27/225, green: 27/225, blue: 58/225, alpha: 1)
    }
    //白系
    class var cultured: UIColor{
        return UIColor(red: 250/225, green: 249/225, blue: 249/225, alpha: 1)
    }
    //グレー系
    class var battleshipGrey: UIColor{
        return UIColor(red: 126/225, green: 137/225, blue: 135/225, alpha: 1)
    }
    //青系
    class var celadonBlue: UIColor{
        return UIColor(red: 36/225, green: 123/225, blue: 160/225, alpha: 1)
    }
    //　オレンジ系
    class var atomicTangerine: UIColor{
        return UIColor(red: 255/225, green: 167/225, blue: 112/225, alpha: 1)
    }
}
